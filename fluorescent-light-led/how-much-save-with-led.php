      <h2 class="sec-title" id="sec-2">電気代削減効果の計算方法</h2>
      <p class="desc">
        ここでは、蛍光灯をLEDに交換した場合の、電気代削減効果の計算方法を説明します。
      </p>
      <h3 class="sub-title" id="sec-2-1">計算式</h3>
      <p class="minor-title">年間電気代削減額 =<br>
        (蛍光灯の消費電力(W) - LEDの消費電力(W)) ÷ 1000 × 1日の点灯時間(h) × 年間点灯日数 × 電気料金単価(円/kWh)
      </p>
      <h3 class="sub-title" id="sec-2-2">計算例：40W 2灯の直管蛍光灯をLEDに交換した場合</h3>
      <p class="desc">(1日8時間点灯、年間250日稼働、電気料金単価: 30円/kWh)</p>
      <p class="minor-title">年間電気代削減額 =<br> (85W - 25W) ÷ 1000 × 8h × 250日 × 30円/kWh = <em>約<span>3,600</span>円</em></p>
      <p class="desc">
        つまり、1台あたり年間約3,600円の電気代を削減できることになります。
        (※ 電気料金単価は、電力会社や契約プランによって異なります。実際の削減額は、ご契約の電力会社の料金単価をご確認ください。)
      </p>
    </section>

