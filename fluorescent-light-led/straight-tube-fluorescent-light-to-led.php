      <p class="desc">
        こちらの写真は、事務所や店舗で最も普及している「屋内天井直付けタイプ、40W 2灯」の直管蛍光灯器具です。このページでは、このような蛍光灯器具をLED化する際に、電気工事業者に工事を依頼した場合の費用相場について、交換方法別（器具交換・バイパス工事）について解説します。
      </p>
