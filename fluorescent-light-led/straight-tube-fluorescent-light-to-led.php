      <p class="desc">
        工事不要のLEDランプは、既存の蛍光灯器具の古い安定器を使い続けることになります。劣化した安定器は、故障の原因となるだけでなく、発熱や発火といった事故を引き起こす可能性もあります。これらのリスクを十分にご理解いただいた上で、交換をご検討ください。
      </p>
      <ul class="desc-list">
        <li>FL (グロースターター式)</li>
        <li>FLR (ラピッドスタート式)</li>
        <li>FHF (インバーター式)</li>
      </ul>
