      <h3 class="sub-title" id="sec-2-2">確認事項2：安定器の確認が最も重要</h3>
      <p class="desc">
        現在ご使用中の蛍光灯ですが、過去のランプ交換で、本来とは異なる種類の蛍光灯が取り付けられている可能性もあります。そのままLEDランプに交換すると、安定器との組み合わせが合わず、不点灯や故障の原因となることも。必ず照明器具のカバーを外し、安定器に記載されている適合ランプの種類を確認しましょう。
      </p>
      <p class="desc">
        <strong>【注意】</strong>照明器具のカバーを外すと、内部の配線など、感電の危険がある箇所が露出している場合があります。作業は必ずブレーカーで電源を切り、安全に十分注意して行ってください。
      </p>
