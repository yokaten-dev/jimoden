      <p class="minor-title">見積書とプランが明確で分かりやすいか</p>
      <p class="desc">
        電気工事の知識がない方にとって、見積書だけで工事内容を完全に理解するのは難しいでしょう。優良な業者は、簡易的でもプラン図を添付するなど、どのようなLED照明器具をどこに設置するのか、移設や増設はどのように行うのかを、視覚的に分かりやすく説明してくれます。 見積書では工事総額だけでなく、工事内容の詳細、そして業者の丁寧な説明や対応にも注目し、総合的に判断することが重要です。
      </p>
      <h3 class="sub-title" id="sec-6-3">③見積りの提出が早い、または提出期限を示す</h3>
      <p class="desc">見積もりの提出が早い、または現地調査の時点で提出予定日を明確に示す業者は、仕事の段取りが良く、信頼できる可能性が高いです。 </p>
      <h3 class="sub-title" id="sec-6-4">④出来る限り近くの業者がおすすめ</h3>
      <p class="desc">現場近くの業者は、移動時間のロスが少ないため、工事の不具合や、その他の電気設備のトラブルにも迅速に対応できる可能性が高く、大きなメリットとなります。 </p>
      <h3 class="sub-title" id="sec-6-5">⑤工事責任賠償保険に加入しているか</h3>
      <p class="desc">工事中に建物や設備を破損させてしまった場合、多額の損害賠償責任を負う可能性があります。万が一の事態に備え、工事賠償責任保険に加入している業者であれば安心です。 これは、事業者自身の経営を守るためにも重要な保険です。</p>
