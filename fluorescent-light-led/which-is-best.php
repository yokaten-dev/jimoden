    <section class="section">
      <h2 class="sec-title" id="sec-3">安定器バイパス工事：メリット・デメリット</h2>
      <p class="desc">
        既存の蛍光灯器具を活用し、安定器をバイパスしてLEDランプを取り付ける方法のメリット・デメリットを解説します。
      </p>
      <figure class="picture">
        <figcaption class="pic-caption">写真左の安定器をバイパス（カット）してLEDランプを設置するバイパス工事施工例</figcaption>
        <img src="img/led-bypass-construction-02.png" alt="蛍光灯器具の安定器をカットし、LEDランプを設置するバイパス工事の施工例">
      </figure>
      <h3 class="sub-title" id="sec-3-1"><span class="header-chevron">メリット</span></h3>
      <h4 class="mid-title">初期費用を抑えられる</h4>
      <p class="desc">既存の蛍光灯器具をそのまま使うため、交換部品はLEDランプのみとなり、工事費用も抑えることができます。特に、埋込型器具や高所作業など、交換に手間がかかる場合ほど、バイパス工事は器具交換に比べて費用面でのメリットが大きくなります。<br>
        また、天井埋込型の器具の場合、適合するLED照明器具が見つからないこともあり、その場合は器具全体の交換に高額な費用がかかってしまいます。このように、特殊な条件下では、バイパス工事が唯一の選択肢となるケースもあります。</p>
      <h4 class="mid-title">工期が短い</h4>
      <p class="desc">既存の照明器具本体はそのまま使用するため、交換工事が不要となり、作業時間を短縮できます。</p>

      <h3 class="sub-title" id="sec-3-2"><span class="header-chevron">デメリット</span></h3>
      <h4 class="mid-title">器具の劣化リスク</h4>
      <p class="desc">長期間使用された蛍光灯器具は、ソケットをはじめとする各部品の劣化が懸念されます。また、器具内の電線も熱によって被覆が劣化していることがあり、注意が必要です。そのまま使用し続けると、将来的にソケット破損によるLEDランプの落下や、接触不良による不点灯といったトラブルが発生するリスクがあります。</p>
      <h4 class="mid-title">国内大手照明メーカーからは販売されていない</h4>
      <p class="desc">現在、パナソニックや東芝などの大手照明メーカーからは、既存の蛍光灯器具に直接取り付け可能なLED直管ランプは販売されていません。これらのメーカーが提供するLED直管ランプは、蛍光灯とは異なる口金を採用しているため、設置できません。<br>蛍光灯器具に取り付け可能なG13口金のLED直管ランプは、コイズミ照明やオーデリックなどの国内メーカーから販売されている製品があります。</p>
      <figure class="picture">
        <figcaption class="pic-caption">蛍光灯の口金G13と大手照明メーカーLED直管ランプの口金GX16-5の違い</figcaption>
        <img src="img/g13-gx16t5-socket-comparison.png" alt="G13ソケットとGX16t-5ソケットの比較画像。互換性はありません。">
      </figure>
    </section>

