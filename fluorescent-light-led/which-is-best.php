    <section class="section">
      <h2 class="sec-title" id="sec-2">LED照明器具への交換：メリット・デメリット</h2>
      <p class="desc">まずは、既存の蛍光灯器具を丸ごと新しいLED照明器具に交換する方法について、メリットとデメリットを解説します。</p>
      <figure class="picture">
        <figcaption class="pic-caption">現在主流のライトバータイプのLED照明器具へ交換した施工例</figcaption>
        <img src="img/led-lightbar-replacement-after.png" alt="天井直付型と埋込型のLED照明器具へ交換した工事後の施工例">
      </figure>
      <h3 class="sub-title" id="sec-2-1"><span class="header-chevron">メリット</span></h3>
      <h4 class="mid-title">全て新品なので長く安全に使用できる</h4>
      <p class="desc">LED照明器具が全て新品となるため、長期間安心して使用できます。また、LEDライトバーの寿命も長いため、交換頻度を抑えられます。</p>
      <h4 class="mid-title">高い安全性</h4>
      <p class="desc">バイパス工事のように既存の器具を全く使用しないので、ソケットの破損や接触不良による事故リスクを回避でき、安全性が高まります。</p>
      <h4 class="mid-title">省エネ効果が高い</h4>
      <p class="desc">最新のLED照明器具は、従来の蛍光灯器具と比べて、大幅に消費電力が削減されていますので、電気代の大幅な節約に繋がります。</p>

      <h3 class="sub-title" id="sec-2-2"><span class="header-chevron">デメリット</span></h3>
      <h4 class="mid-title">初期費用が高額</h4>
      <p class="desc">LED照明器具本体、交換工事費、蛍光灯照明器具本体の運搬と処分費がかかるため、バイパス工事と比べて初期費用が高額となります。</p>
      <p class="desc">一般的な作業環境における、40W 2灯の直付け直管蛍光灯をLEDへ切り替える場合の２通りの工事費用総額をご紹介します。<br></p>
