    <section class="section">
      <h2 class="sec-title" id="sec-5">最適なのはどっち？工事方法を選ぶポイント</h2>
      <p class="desc">LED器具交換とバイパス工事、どちらを選ぶべきかは、状況と何を重視するかによって異なります。</p>
      <h3 class="sub-title" id="sec-5-1">LED器具交換がおすすめの方</h3>
      <ul class="desc-list">
        <li>築年数が古い建物で、既存の蛍光灯器具がかなり劣化している</li>
        <li>安全性や信頼性を最優先したい</li>
        <li>最新のLED照明器具のデザインや機能にこだわりたい</li>
        <li>長期的な省エネ効果を重視する</li>
      </ul>
      <h3 class="sub-title" id="sec-5-2">安定器バイパス工事がおすすめの方</h3>
      <ul class="desc-list">
        <li>初期費用をできるだけ抑えたい</li>
        <li>既存の蛍光灯器具の状態が比較的良好な場合</li>
        <li>最新のLED照明器具のデザインや機能にこだわりたい</li>
        <li>工事を短時間で終わらせたい</li>
      </ul>
    </section>

