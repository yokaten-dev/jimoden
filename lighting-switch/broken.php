      <h2 class="sec-title" id="sec-1">電気スイッチが故障？まずはここを確認【簡単３ステップ診断】</h2>
      <p class="desc">
        「もしかして故障…？」と思ったら、まずはご自身で簡単にチェックしてみましょう。 <br>次の３ステップで原因を特定できます。
      </p>
      <h3 class="sub-title" id="sec-1-1">ステップ１：スイッチ本体に異常がないか確認</h3>
      <p class="desc">
        以下の症状がある場合、ほぼ確実にスイッチ本体の故障です。 電気工事業者へ交換を依頼しましょう。
      </p>
      <ul class="desc-list">
        <li>押せない</li>
        <li>勝手に戻る</li>
        <li>押した感触が弱い、カチッと音がしない</li>
        <li>スイッチが熱い</li>
        <li>焦げたようなにおいがする</li>
        <li>変色したり、破損している</li>
      </ul>
      <p class="desc"><strong>【要注意！】パナソニック製ワイド21スイッチの「バネ交換」はおすすめしません</strong></p>
      <p class="desc">※パナソニック製ワイド21のスイッチでは、押せない、押した感触が弱いといった症状を、内部のバネを交換することで改善できる場合があります。しかし、この行為はメーカーは認めておらず、安全上のリスクを伴う可能性があります。 また、多くの電気工事業者は、安全上の理由から、バネ交換ではなくスイッチ本体の交換を行います。<br>バネ交換を検討される場合は、これらのリスクを十分に理解した上で、自己責任において行ってください。ただし、感電や火災、故障の原因となる可能性があり、メーカー保証も受けられなくなるため、推奨いたしません。 安全のため、電気工事業者にご相談いただき、スイッチ本体の交換をご検討ください。</p>
