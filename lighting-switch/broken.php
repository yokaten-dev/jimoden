      <h3 class="sub-title" id="sec-1-3">ステップ３：電球・蛍光灯が切れていないか確認</h3>
      <p class="desc">電球、蛍光灯、グローランプ（点灯管）の球切れを確認しましょう。</p>
      <p>確認方法:</p>
      <ul class="desc-list">
        <li><b>電球・蛍光灯:</b><br>お手持ちの新しい電球や正常に点灯する別の照明器具から外した電球と交換し、点灯するか確認してください。</li>
        <li><b>グローランプ（点灯管）:</b><br>蛍光灯照明器具の場合、グローランプも新しいものに交換して点灯を確認してください。</li>
      </ul><br>
      <p>確認後の対応:</p>
      <ul class="desc-list">
        <li><b>球切れで点灯した場合:</b><br>球切れが原因でしたので、新しい電球やグローランプに交換してください。</li>
        <li><b>交換しても点灯しない場合:</b><br>球切れではないため、以下の原因が考えられます。
          <ul class="list">
            <li><b>蛍光灯の場合: </b><br>安定器の故障</li>
            <li><b>電球の場合: </b><br>スイッチ本体、または配線の故障</li>
          </ul>
        </li>
      </ul>
      <br>
