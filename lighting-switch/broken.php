      <p class="sub-title">Ｑ：自分でスイッチの部材を用意した方が安くなりますか？</p>
      <p class="minor-title">
        Ａ：いいえ、基本的に安くなることはありません。 なぜなら、ご自身で部材をご用意される場合、以下のような理由から、かえって損をしてしまう可能性があるためです。
      </p>
      <ul class="list">
        <li><b>適合性：</b><br>スイッチには様々な種類があり、ご家庭の電気設備に適合しないものを選んでしまうリスクがあります。間違った部材を購入してしまうと工事自体ができなくなってしまい、部材の無駄になってしまいます。</li>
        <li><b>保証：</b><br>電気工事業者によっては、工事に使用する部材も保証の対象としている場合があります。業者に部材も手配してもらうことで、万が一、初期不良や早期故障が起きた場合でも、無償で対応してもらえる可能性があります。ご自身で用意された部材の場合、保証対象外となることが一般的です。</li>
        <li><b>手間：</b><br>ご自身で部材を選び、購入する手間も考慮する必要があります。スイッチの種類は多岐にわたり、適合するものを探すのは意外と時間がかかるものです。また、不良品だった場合、返品・交換の手間も発生します。</li>
      </ul>
      <p class="desc">
        以上の理由から、スイッチ交換を業者に依頼される場合は、業者に部材を手配してもらうのが、費用面、手間面、安心面でもおすすめです。
      </p>

      <p class="sub-title">Ｑ：複数の業者に見積もりを取るべきですか？</p>
      <p class="minor-title">
        Ａ：相見積もりは有効ですが、何度も立ち会うのは大変です。 メールやLINEで写真などを送って見積もりを出してくれる業者を選ぶと良いでしょう。
      </p>
      <p class="desc">
        ただし、写真だけでは判断が難しい場合もあります。 その場合は、ある程度幅を持たせた見積もり（例：8,800円＋部材費300円～1,000円） を提示してくれる業者なら、信頼できる可能性が高いです。私なら、スイッチやコンセントの交換といった軽微な工事で、 なるべく現地へ見積りに行きたくありません。時間の調整も必要ですし。 スイッチ交換などの比較的短時間で完了する工事で、 見積もりのために現地へ行くのは正直なところ避けたい、 というのが本音です。ですからヒアリングや画像で工事費用の総額をズバッと言います。
      </p>

