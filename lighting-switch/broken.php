      <h3 class="sub-title" id="sec-1-2">ステップ２：ブレーカーが落ちていないか確認</h3>
      <p class="desc">
        スイッチだけでなく、同じ部屋の照明やコンセントも使えない場合は、 ブレーカーが落ちている可能性があります。 <br>分電盤を確認し、落ちているブレーカーがあれば「入」にしてください。<br>
        ※稀に小さなブレーカーが工事ミスで上下反対に取り付けてあることがあります。その場合は【入】と【切】が上下反対になります。
      </p>
