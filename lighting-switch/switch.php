    <section class="section">
      <h2 class="sec-title" id="sec-2">電気工事業者の修理が必要な電気スイッチ故障の症状</h2>
      <p class="sec-lead">ここからご紹介するのは、電気スイッチの故障時に起こりやすい主な4つの症状です。これらの症状の場合スイッチの交換が必要となるため、電気工事士の資格がなければ作業ができません。スイッチ交換は電気工事の中でも最も簡単な工事の一つで、誰でもできそうに思われるかもしれませんが、法律に違反するだけではなく、漏電や感電の危険もあるため、必ず専門業者に依頼するようにしてください。</p>
      <h3 class="sub-title" id="sec-2-1">①スイッチが戻らない、勝手に戻る</h3>
      <figure class="picture"><img src="img/switch-symptoms-of-failure01.jpg" alt="フルカラースイッチが戻らない、勝手に戻ってしまうことが多い" /></figure>
      <p class="desc">写真のような左右に倒すスイッチで、スイッチが戻せなくなったり、勝手に戻ってしまったりする場合は、スイッチ本体の故障のため交換が必要です。電気工事業者に症状とスイッチの仕様を伝え、修理を依頼してください。</p>
      <h3 class="sub-title" id="sec-2-2">②スイッチを押しても入り切りできない</h3>
      <figure class="picture"><img src="img/switch-symptoms-of-failure02.jpg" alt="スイッチを押しても入り切りできないのはワイド２１スイッチに多い" /></figure>
      <p class="desc">写真のように押すたびに入り切りするタイプのスイッチでは、内部の板バネが劣化し押しても感触が弱くなってしまったり、正常に入り切りできなくなったりすることがあります。板バネのみの交換はメーカーにより禁止されているため、スイッチ全体の交換が必要です。
        電気工事業者に症状とスイッチの仕様を伝え、修理を依頼してください。</p>
      <div class="reference">
        <p class="site-link">参考：<a href="https://jpn.faq.panasonic.com/app/answers/detail/a_id/80551/related/1" target="_blank" rel="noopener noreferrer">パナソニックホームページ：【スイッチ】コスモシリーズワイド21やアドバンスシリーズのスイッチで本体内部の板バネのみの交換、もしくは補修用部材はありますか？</a></p>
      </div>
      <h3 class="sub-title" id="sec-2-3">③スイッチが中に入ってしまった</h3>
      <figure class="picture"><img src="img/switch-symptoms-of-failure03.jpg" alt="電気スイッチが中に入ってしまった状態（陥没）" /></figure>
      <p class="desc">スイッチを強く押してしまった場合や、施工不良によりスイッチの固定が甘い場合などでは、長年の使用によりスイッチが中に入ってしまうことがあります。
        このような場合は、スイッチの再固定が必要です。
        「再固定」ではありますが、法律上は「スイッチの設置」にあたるため、電気工事士の資格が必要な工事となります。</p>
      <h3 class="sub-title" id="sec-2-4">④スイッチが熱い</h3>
      <figure class="picture"><img src="img/switch-symptoms-of-failure04.jpg" alt="接触不良によりスイッチが熱くなった場合はすぐにスイッチ交換が必要" /></figure>
      <p class="desc">スイッチの接点が劣化して接触不良を起こしていると、スイッチが熱を持つことがあります。このような場合は絶対にスイッチを入れずに、すぐに電気工事業者へ連絡しましょう。火災の危険があるため、急いで修理をする必要があります。</p>
    </section>
    <section class="section">
      <h2 class="sec-title" id="sec-3">電気スイッチ交換を業者へ依頼した場合の交換費用</h2>
      <h3 class="sub-title" id="sec-3-1">相場は1ヵ所6,600円~9,900円(税込)＋スイッチ部品代</h3>
      <p class="desc">電気工事業者へ直接依頼をする場合のスイッチ交換費用は、1ヵ所6,600円~9,900円(税込)＋スイッチ部品代です。 業者により出張費、工事費などの項目がありますが、近くの電気工事業者に依頼した場合、相場は9,000円～10,000円が一般的なスイッチの交換費用（総額）となります。 （※電気工事業者30社へ聞き取りを行い、確認した金額の平均値です。） </p>
      <p class="mid-title">下記の場合は費用が高くなります</p>
      <ul class="desc-list">
        <li>ハウスメーカーや工務店へ依頼。</li>
        <li>人感センサー付き、遅れタイマー付きなどの高機能スイッチへ取り替える場合。</li>
        <li>遠くの電気工事業者へ依頼した場合。</li>
      </ul>
    </section>
    <section class="section">
      <h2 class="sec-title" id="sec-4">電気スイッチ交換はどこへ依頼できる？</h2>
      <h3 class="sub-title">①近くの電気工事業者</h3>
      <p class="desc">電気スイッチ交換を依頼するのに最もおすすめなのは、地元の電気工事業者へ直接依頼をすることです。<br />地元の電気工事業者に依頼をするのが一番「早くて安い」のです。<br />たとえばホームセンターや家電量販店などでも修理依頼を受け付けていますが、お店で受付を行うのは電気工事の知識が無い人になってしまい、「スムーズに話が進まない」「下見をして見積もりを出すため出張料がかかってしまう」など不便な点が発生します。一方で電気工事業者に直接問い合わせをすれば、スイッチ交換のような単純な電気工事であれば問い合わせ時点でほぼ正確な工事費用を算出することができ、さらに近所であれば即日工事に取りかかれることも少なくありません。
      </p>
      <a class="local-link" href="https://jimoden.jp/lighting-switch/"><span>スイッチ交換の料金表がある地元の電気工事業者とつながる『ジモデン』</span></a>
      <h3 class="sub-title">②住宅メーカー・建築会社</h3>
      <p class="desc">自宅を建てた際に利用した住宅メーカー・建築会社や、有名なメーカーであれば安心感や信頼感があることでしょう。しかし、住宅メーカーや建築会社に依頼をした場合、メーカーから電気工事業者に依頼をすることになります。紹介料や仲介手数料が上乗せされるだけでなく、工事まで日数がかかってしまうため、あまりおすすめではありません。
      </p>
      <h3 class="sub-title">③マッチングサイト</h3>
      <p class="desc">くらしのマーケット、ミツモアなどのマッチングサイトが電気工事では有名です。 多くの業者が登録されており、料金や口コミを見ることができるため安心できると感じる人も多いでしょう。 また、業者同士で価格競争が起こりやすい仕組みになっているため、一般的な相場よりも費用を抑えて利用できることが多いのもメリットの一つです。 ただし、業者とトラブルや行き違いが発生した場合、サイトの運営者に責任を持った対応を取ってもらうことは難しく、当事者間で解決する必要があるため注意しなければなりません。</p>
    </section>
    <section class="section">
      <h2 class="sec-title" id="sec-5">電気工事のプロがスイッチ交換を依頼するとしたらどうする？</h2>
      <h3 class="sub-title">近くの電気工事業者へ依頼する</h3>
      <p class="desc">近く（片道20km程度）の電気工事業者への依頼が最もおすすめです。近くであれば即日工事をしてもらえることも珍しくなく、また出張費の請求もほとんどの場合ありません。<br />電気スイッチ交換のような少額な電気工事の場合、片道30km以上もあるような遠方の業者に依頼をしてしまうと、業者としては時間と交通費（ガソリン代）が必要なため、他の用事で近くに行く際にまとめて工事をしたいと考えてしまいます。そのためお客様の都合に合わせることや、すぐに工事を行うことが難しくなります。
        また、ハウスメーカーや建築会社、ホームセンターや家電量販店などへ依頼することもできますが、最終的に工事を行うのは電気工事業者となるため、時間と費用が余分にかかるだけとなりおすすめできません。
      </p>
      <h3 class="sub-title">相見積もりを行うなら電話やメールで</h3>
      <p class="desc">電気工事業者にとっては、一般的なスイッチ交換は単純な工事です。そのため電話やメールで症状やスイッチの種類をヒアリングする、写真を送ってもらうことで工事費用の総額を出すことができます。（※「現地を見ないとわかりません」という業者は高額な請求を行う危険性があるため、避けた方が良いです。）
        スイッチ交換程度であれば、現地見積もりを複数社にしてもらうのは時間と労力の割に合いません。
        電話やメールで工事費用を回答してくれた業者の中から安い業者を選ぶのが良いでしょう。
      </p>
      <h3 class="sub-title">部品を自分で用意したら安く抑えられる？</h3>
      <p class="desc">スイッチ部材を自分で用意したら安くなるのではないかと考える人も少なくないでしょう。しかし、ホームセンターや家電量販店でスイッチ部材を購入しても、ほぼ定価のため費用を抑えることにはつながりません。また、スイッチの種類は多いため、知識のない方が無理をして購入すると、間違ったスイッチを買ってしまうリスクがあり、却って出費が高くなってしまいます。そのため、スイッチ部材は電気工事業者に用意してもらうのが確実です。</p>
    </section>

