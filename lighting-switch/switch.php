      <h2 class="sec-title" id="sec-1">電気スイッチの故障 自分でDIYができる症状とやり方</h2>
      <h3 class="sub-title" id="sec-1-1">①スイッチ全体がぐらぐらする場合の修理方法</h3>
      <figure class="picture"><img src="img/wobble-switch.jpg" alt="スイッチがぐらぐらしている場合はネジを締めるだけで直せる" /></figure>
      <p class="desc"> スイッチ全体がぐらぐらしている場合は、ネジが緩んでいるだけなので誰でも簡単に直すことができます。まずは外側のプレートを外し、中のネジを露出させます。基本は写真のようにプラスドライバーで直せるネジですが、古いスイッチの場合マイナスドライバーが必要な場合もあります。ご自宅のスイッチのネジに合わせて、必要なドライバーを判断してください。
        目視でスイッチの土台が垂直になっていることを確認したうえで、写真の水色のネジを2ヵ所締めればしっかりと固定でき、修理の完了です。
      </p>
      <div class="reference">
        <p class="site-link">参考：<a href="https://www.youtube.com/watch?v=fb3CT10F1pk" target="_blank" rel="noopener noreferrer">【簡単】コンセントが"ぐらぐら"する場合の対処法</a></p>
      </div>
      <h3 class="sub-title" id="sec-1-2">②スイッチプレートが割れた場合の修理方法</h3>
      <figure class="picture"><img src="img/broken-switch-plate.jpg" alt="割れたスイッチプレートは誰でも簡単に交換できる" /></figure>
      <p class="desc">スイッチプレートが割れてしまった場合も、修理は自分でできます。
        まずはスイッチプレートの表面を外してホームセンターなどへ持って行き、同じ品番のものを購入しましょう。新しいスイッチプレートを用意したら、写真の水色のネジを緩めてプレートを取り外します。
        新しいプレートに取り替えてビスを締めれば修理の完了です。※一般的なスイッチの場合、パナソニック製であることがほとんどです。しかし稀に東芝製、神保製のものの場合があります。これらの製品元の場合お店での取り扱いがないこともありますので、手配可能か確認してください。
      </p>
      <div class="reference">
        <p class="site-link">参考：<a href="https://www.youtube.com/watch?v=vg-1xCf284k" target="_blank" rel="noopener noreferrer">コンセント・スイッチのプレート交換・取替え　電気工事</a></p>
      </div>
